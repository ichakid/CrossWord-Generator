    private void startBoard(){
        char[][] arr = new char[9][9];
        for (int i =0; i<9; i++){
            for (int j=0; j<9; j++){
                arr[i][j] = ' '; 
            }
        }
        arr[1][1] = 'W';
        arr[2][1] = 'O';
        arr[3][0] = 'C'; arr[3][1] = 'R'; arr[3][2] = 'O'; arr[3][3] = 'S'; arr[3][4] = 'S';
        arr[4][1] = 'D';
        arr[6][0] = 'G';arr[6][1] = 'E';arr[6][2] = 'N';arr[6][3] = 'E';arr[6][4] = 'R';arr[6][5] = 'A';arr[6][6] = 'T';arr[6][7] = 'O';arr[6][8] = 'R';
        boardLayout = new boardPanel(9, arr);
        jScrollPane1.setViewportView(boardLayout);
    }
    
