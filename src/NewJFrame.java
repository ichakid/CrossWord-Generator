    private int getIndex(String str, int idx){
        String sel = "";
        if (str == "hor"){
            sel = TTS.hWords[idx];
        } else if (str == "ver"){
            sel = TTS.vWords[idx];
        }
        int k = sel.indexOf(". ");
        String numStr = sel.substring(0, k);
        int length = sel.substring(k+2).length();
        int num = Integer.parseInt(numStr);
        for (int i=0; i<TTS.boardSize; i++){
            for (int j=0; j<TTS.boardSize; j++){
                if (TTS.emptyBoard[i][j] == num){
                    abs = j; ord = i;
                }
            }
        }
        return length;
    }
